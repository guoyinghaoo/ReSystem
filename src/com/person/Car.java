package com.person;

public class Car {
	private String cainame;
	private int caiprice;
	private int id;
	private String username;
	
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCainame() {
		return cainame;
	}
	public void setCainame(String cainame) {
		this.cainame = cainame;
	}
	public int getCaiprice() {
		return caiprice;
	}
	public void setCaiprice(int caiprice) {
		this.caiprice = caiprice;
	}
	

}
